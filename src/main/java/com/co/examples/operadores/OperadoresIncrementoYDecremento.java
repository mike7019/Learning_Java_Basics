package com.co.examples.operadores;

public class OperadoresIncrementoYDecremento {

    public static void main(String[] args) {

        int numero = 10;

        System.out.println("El numero es: " + numero); //10
        numero++; //10+1
        System.out.println("El numero es: " + numero); //11
        numero++; //11+1
        System.out.println("El numero es: " + numero); //12
        --numero; //12-1
        System.out.println("El numero es: " + numero);//11
    }

}
