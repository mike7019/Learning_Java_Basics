package com.co.examples.operadores;

public class OperadoresRelacionales {

    public static void main(String[] args) {
        int x = 21;
        int y = 20;

        System.out.println("x == y : " + (x == y)); //comparacion
        System.out.println("x != y : " + (x != y));
        System.out.println("x < y : " + (x < y));
        System.out.println("x > y : " + (x > y));
        System.out.println("x <= y : " + (x <= y));
        System.out.println("x >= y : " + (x >= y));
    }

}
