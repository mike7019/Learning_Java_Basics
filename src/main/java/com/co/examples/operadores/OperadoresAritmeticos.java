package com.co.examples.operadores;

public class OperadoresAritmeticos {


    public static void main(String[] args) {
        int a = 20;
        int b = 3;

        System.out.println("La suma de " + a + " y " + b + " es: " + (a + b));
        System.out.println("La resta de " + a + " y " + b + " es: " + (a - b));
        System.out.println("La multiplicacion de " + a + " y " + b + " es: " + (a * b));
        System.out.println("La division de " + a + " y " + b + " es: " + (double) (a / b));
        System.out.println("La modulo de " + a + " y " + b + " es: " + (a % b));
    }

}
